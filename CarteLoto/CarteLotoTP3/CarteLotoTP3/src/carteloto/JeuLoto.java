    private void BCouleurActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCouleurActionPerformed
        int ic;
        int nbCol=c.getNbColonnes();
        do{ ic = (int) (Math.random()*tabCouleur.length);
        } while (this.coul.equals(tabCouleur[ic]));
        this.coul= tabCouleur[ic];
        for (int ind= 0; ind< c.getNbLignes()*nbCol; ind++)
        {  JButton jb = (JButton) PanCarte.getComponent(ind);
           jb.setBackground(tabCouleur[ic]);
        }
        
    }//GEN-LAST:event_BCouleurActionPerformed

