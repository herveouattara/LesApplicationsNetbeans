     public void afficheValeurs()
     { int nbCol=c.getNbColonnes();
       for (int ind= 0; ind< nbCol*c.getNbLignes(); ind++)
        {  JButton jb = (JButton) PanCarte.getComponent(ind);
            int i= ind/nbCol;
            int j= ind - i*nbCol;
            if (c.getCase(i, j)!=0) jb.setText(""+c.getCase(i, j));
            else jb.setText("");
        }
    }
     
     
